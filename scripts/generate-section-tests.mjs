#!/usr/bin/env node
/**
 * generate-section-tests.mjs — Auto-generate Section component test scaffolds.
 *
 * Scans Section .tsx files for getField/setField calls, extracts the field
 * paths, and generates vitest test files with render + interaction tests.
 *
 * Usage:
 *   node scripts/generate-section-tests.mjs                     # preview all
 *   node scripts/generate-section-tests.mjs --section Agents     # single section
 *   node scripts/generate-section-tests.mjs --write              # write files
 *   node scripts/generate-section-tests.mjs --version 2026.4.6   # tag with version
 *
 * Output: web/tests/sections-<version>.test.tsx (or sections-auto.test.tsx)
 */

import { readFileSync, writeFileSync, existsSync, readdirSync } from 'fs';
import { resolve, basename } from 'path';

const ROOT = resolve(new URL('.', import.meta.url).pathname.replace(/^\/([A-Z]:)/, '$1'), '..');
const SECTIONS_DIR = resolve(ROOT, 'web', 'windows', 'Editor', 'sections');
const TESTS_DIR = resolve(ROOT, 'web', 'tests');

// ─── CLI args ───
const args = process.argv.slice(2);
const writeMode = args.includes('--write');
const sectionFilter = args.includes('--section') ? args[args.indexOf('--section') + 1] : null;
const version = args.includes('--version') ? args[args.indexOf('--version') + 1] : 'auto';

// ─── Extract field info from Section source ───
function extractFields(content, sectionName) {
  const fields = [];

  // Match label patterns: label={es.someKey || 'Fallback Label'}
  const labelRegex = /label=\{(?:es\.(\w+)\s*\|\|\s*)?['"]([^'"]+)['"]\s*\}/g;
  let m;
  while ((m = labelRegex.exec(content)) !== null) {
    fields.push({ i18nKey: m[1] || null, fallback: m[2] });
  }

  // Match label={es.someKey} (without fallback)
  const labelOnlyRegex = /label=\{es\.(\w+)\}/g;
  while ((m = labelOnlyRegex.exec(content)) !== null) {
    // Skip duplicates
    if (!fields.some(f => f.i18nKey === m[1])) {
      fields.push({ i18nKey: m[1], fallback: null });
    }
  }

  return fields;
}

// ─── Detect component type patterns ───
function detectFieldTypes(content) {
  const types = {
    textFields: 0,
    numberFields: 0,
    selectFields: 0,
    switchFields: 0,
    arrayFields: 0,
    passwordFields: 0,
    kvFields: 0,
  };
  types.textFields = (content.match(/<TextField\b/g) || []).length;
  types.numberFields = (content.match(/<NumberField\b/g) || []).length;
  types.selectFields = (content.match(/<SelectField\b/g) || []).length;
  types.switchFields = (content.match(/<SwitchField\b/g) || []).length;
  types.arrayFields = (content.match(/<ArrayField\b/g) || []).length;
  types.passwordFields = (content.match(/<PasswordField\b/g) || []).length;
  types.kvFields = (content.match(/<KeyValueField\b/g) || []).length;
  return types;
}

// ─── Detect section path prefix ───
function detectSectionPath(content) {
  // Match: const g = (p: string[]) => getField(['tools', ...p]);
  const helperRegex = /=>\s*getField\(\[['"](\w+)['"]/;
  const m = helperRegex.exec(content);
  return m ? m[1] : null;
}

// ─── Check for SchemaRemainder usage ───
function hasSchemaRemainder(content) {
  return content.includes('SchemaRemainder');
}

// ─── Generate test code ───
function generateTest(sectionName, exportName, fields, types, sectionPath, hasRemainder) {
  const totalFields = Object.values(types).reduce((a, b) => a + b, 0);

  // Pick representative fields for label rendering tests (max 5)
  const labelTests = fields
    .filter(f => f.fallback)
    .slice(0, 5);

  const lines = [];

  // Header
  lines.push(`// Auto-generated test scaffold for ${sectionName}`);
  lines.push(`// Generated by scripts/generate-section-tests.mjs`);
  lines.push('');

  // Individual section test block
  lines.push(`describe('${sectionName}', () => {`);

  // Basic render test
  lines.push(`  it('renders without crashing', () => {`);
  lines.push(`    const { container } = render(<Wrap><${exportName} {...props} /></Wrap>);`);
  lines.push(`    expect(container).toBeTruthy();`);
  lines.push(`  });`);
  lines.push('');

  // Label rendering tests
  for (const field of labelTests) {
    const label = field.fallback;
    const safeLabel = label.replace(/'/g, "\\'");
    lines.push(`  it('renders ${label} field', () => {`);
    lines.push(`    render(<Wrap><${exportName} {...props} /></Wrap>);`);
    lines.push(`    expect(screen.getByText('${safeLabel}')).toBeInTheDocument();`);
    lines.push(`  });`);
    lines.push('');
  }

  // Field count summary test
  lines.push(`  it('has expected number of field components (${totalFields})', () => {`);
  lines.push(`    // ${types.textFields} TextField, ${types.numberFields} NumberField, ${types.selectFields} SelectField, ${types.switchFields} SwitchField, ${types.arrayFields} ArrayField`);
  lines.push(`    const { container } = render(<Wrap><${exportName} {...props} /></Wrap>);`);
  lines.push(`    // Verify at least some inputs are rendered`);
  lines.push(`    const inputs = container.querySelectorAll('input, select, textarea');`);
  lines.push(`    expect(inputs.length).toBeGreaterThan(0);`);
  lines.push(`  });`);

  // SchemaRemainder integration test
  if (hasRemainder) {
    lines.push('');
    lines.push(`  it('includes SchemaRemainder for auto-discovered fields', () => {`);
    lines.push(`    // SchemaRemainder renders when schema has properties not in handledKeys`);
    lines.push(`    const { container } = render(<Wrap><${exportName} {...props} /></Wrap>);`);
    lines.push(`    expect(container).toBeTruthy();`);
    lines.push(`  });`);
  }

  lines.push('});');

  return lines;
}

// ─── Main ───
function main() {
  const sectionFiles = readdirSync(SECTIONS_DIR)
    .filter(f => f.endsWith('Section.tsx') && !f.startsWith('Json') && !f.startsWith('Live') && !f.startsWith('Unmapped'))
    .sort();

  console.log(`\n📋 Section Test Generator\n`);
  console.log(`Found ${sectionFiles.length} section files\n`);

  const allTestBlocks = [];
  const imports = new Set();

  for (const file of sectionFiles) {
    const name = basename(file, '.tsx');
    if (sectionFilter && !name.toLowerCase().includes(sectionFilter.toLowerCase())) continue;

    const content = readFileSync(resolve(SECTIONS_DIR, file), 'utf8');

    // Detect export name
    const exportMatch = content.match(/export\s+(?:const|{)\s*(\w+Section)/);
    const exportName = exportMatch ? exportMatch[1] : name;

    const fields = extractFields(content, name);
    const types = detectFieldTypes(content);
    const sectionPath = detectSectionPath(content);
    const hasRemainder = hasSchemaRemainder(content);
    const totalFields = Object.values(types).reduce((a, b) => a + b, 0);

    console.log(`  ${name.padEnd(25)} ${totalFields} fields, ${fields.filter(f => f.fallback).length} with fallback labels${hasRemainder ? ' [+SchemaRemainder]' : ''}`);

    imports.add(exportName);
    const testLines = generateTest(name, exportName, fields, types, sectionPath, hasRemainder);
    allTestBlocks.push(testLines.join('\n'));
  }

  // ─── Build full test file ───
  const importLines = Array.from(imports).map(name => {
    const fileName = name.replace(/Section$/, 'Section');
    return `const { ${name} } = await import('../windows/Editor/sections/${fileName}');`;
  });

  const fullFile = [
    `/**`,
    ` * Auto-generated section tests (version: ${version})`,
    ` * Generated by: node scripts/generate-section-tests.mjs`,
    ` * Date: ${new Date().toISOString().slice(0, 10)}`,
    ` *`,
    ` * These tests verify that Section components render correctly`,
    ` * and include SchemaRemainder for auto-discovered fields.`,
    ` */`,
    `import { describe, it, expect, vi } from 'vitest';`,
    `import { render, screen } from '@testing-library/react';`,
    `import React from 'react';`,
    ``,
    `// Dynamic imports for lazy-loaded sections`,
    ...importLines,
    ``,
    `// Mock SectionProps`,
    `const props = {`,
    `  config: {},`,
    `  schema: null,`,
    `  setField: vi.fn(),`,
    `  getField: vi.fn(() => undefined),`,
    `  deleteField: vi.fn(),`,
    `  appendToArray: vi.fn(),`,
    `  removeFromArray: vi.fn(),`,
    `  language: 'en' as const,`,
    `  save: vi.fn(),`,
    `};`,
    ``,
    `function Wrap({ children }: { children: React.ReactNode }) {`,
    `  return <>{children}</>;`,
    `}`,
    ``,
    ...allTestBlocks.join('\n\n').split('\n'),
    ``,
  ].join('\n');

  if (writeMode) {
    const outPath = resolve(TESTS_DIR, `sections-${version}.test.tsx`);
    if (existsSync(outPath)) {
      console.log(`\n⚠️  File already exists: ${outPath}`);
      console.log('   Delete it first or use a different --version');
      process.exit(1);
    }
    writeFileSync(outPath, fullFile, 'utf8');
    console.log(`\n✅ Written: web/tests/sections-${version}.test.tsx`);
  } else {
    console.log('\n─── Preview ───\n');
    console.log(fullFile);
    console.log('\n💡 Run with --write to create the test file');
  }
}

main();
